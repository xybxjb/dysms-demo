	/**
	 * 添加位置信息
	 * @param key
	 * @param longitude 经度
	 * @param latitude 纬度
	 * @param member
	 * @return
	 */
	public long geoAdd(Object key, Double longitude,Double latitude,Object member) {
		Long addNum = redisTemplate.opsForGeo().add(key,new Point(longitude,latitude),member);
		return addNum;
	}

	/**
	 * 删除 member
	 * @param key
	 * @param member
	 * @return
	 */
	public long deleteMember(Object key, Object member) {
		Long addNum = redisTemplate.opsForZSet().remove(key,member);
		return addNum;
	}

    /***
     * 以给定的经纬度为中心， 返回键包含的位置元素当中， 与中心的距离不超过给定最大距离的所有位置元素，并给出所有位置元素与中心的平均距离。
     * @param key redis的key
     * @param longitude 经度
     * @param latitude 纬度
     * @param distance 距离
     * @param count 数量
     * @return
     */
    public GeoResults<RedisGeoCommands.GeoLocation<String>> redisNearByXY(String key, double longitude, double latitude, Integer distance, Integer count) {
        Circle circle = new Circle(new Point(longitude, latitude), new Distance(distance, Metrics.KILOMETERS));//Point(经度, 纬度) Distance(距离量, 距离单位)
        RedisGeoCommands.GeoRadiusCommandArgs args = RedisGeoCommands.GeoRadiusCommandArgs.newGeoRadiusArgs().includeDistance().includeCoordinates().sortAscending().limit(count);
        GeoResults<RedisGeoCommands.GeoLocation<String>> results = redisTemplate.opsForGeo()
                .radius(key, circle, args);//params: key, Circle, GeoRadiusCommandArgs
        return results;
    }

	/***
	 * 从key里返回所有给定位置元素的位置（经度和纬度）。
	 * @param key redis 的 key
	 * @param nameList 名称的集合
	 */
	public List<Point> redisGeoGet(String key, Integer nameList) {
		List<Point> points = redisTemplate.opsForGeo().position(key,nameList);//params: key, 地方名称...
		return points;
	}

	/***
	 * 返回两个给定位置之间的距离。
	 * @param key redis的key
	 * @param member1 名称1
	 * @param member2 名称2
	 * @return
	 */
	public Distance geoDist(Object key, Object member1, Object member2) {
		Distance distance = redisTemplate.opsForGeo()
				.distance(key, member1, member2, RedisGeoCommands.DistanceUnit.KILOMETERS);//params: key, 名称1, 名称2, 距离单位
		return distance;
	}

